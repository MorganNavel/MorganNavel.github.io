        <h1>내 사랑</h1>
        <div class="poem">
            <h2>Mon Premier Poème</h2>
            <p>
                단비, tes yeux pour moi n'ont nulle autre pareil<br>
                단비, ton unique voix tous les jours m'émerveille<br>
                딘비, ta présence me rempli de bonheur<br>
                Cela me suffit à évacuer mes peurs<br>
                Ton simple nom me comble d'une immense joie<br>
                <br>
                J'aimerai un jour peut-être vivre avec toi<br>
                Mon âme soeur, mon étoile, mon rayon de soleil<br>
                Ton sourire radieux, en moi l'amour s'éveille.<br>
                Vers l'horizon de nos vies, notre amour s'éveille,<br>
                Et dans ce doux voyage, je t'appellerai ma femme éternelle.<br>
            </p>

        </div>

        <div class="poem">
            <h2>Mon Deuxième Poème</h2>
            <p>
                À toi, 단비, mon amour précieux,<br>
                Cent jours de bonheur, déjà parcourus à deux.<br>
                Trois mois de complicité, de joie partagée,<br>
                Chaque instant avec toi, une éternité.<br>
                <br>
                Dans tes bras, je trouve la quiétude,<br>
                Nos cœurs unis, une tendre certitude.<br>
                Chaque jour, notre amour grandit en ampleur,<br>
                Une promesse d'éternité, un pur bonheur.<br>
                <br>
                Main dans la main, vers l'horizon nous avançons,<br>
                Un amour sincère, sans aucune illusion.<br>
                De chaque instant, je suis éperdument épris,<br>
                Cent jours de magie, je te le redis.<br>
                
                À toi, ma douce étoile, mon rayon de soleil,<br>
                Je célèbre ces moments, précieux et sans pareil.<br>
                <br>
                Avec tout mon amour,<br>
                Morgan <br>
            </p>
        </div>

        <div class="poem">
            <h2>Mon Troisème Poème</h2>
            <p>
                Danbee avec toi ma vie n'est que bonheur,<br>
                Sans toi ma vie serait hanté par la peur,<br>
                Je veux que toi et moi nos coeur s'unissent,<br>
                Afin que dans notre vie on s'épanouissent,<br>
                Dans ce doux serment, nos destins se tissent,<br>
                <br>
                Comme le début de l'aube, tu t'installes,<br>
                Réchauffant mon cœur, tel le printemps après l'hiver,<br>
                Toi déesse de l'amour, Vénus, tu incarnes,<br>
                Sous l'astre de tes yeux, mon coeur se désarme,<br>
                Pour laisser place à ce doux feu qui s'enflamme,<br>
                <br>
                Morgan NAVEL, ton futur mari
            </p>
        </div>
